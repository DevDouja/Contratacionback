DROP TABLE CATASTROS	IF EXISTS;
DROP TABLE DIRECCIONES	IF EXISTS;

CREATE TABLE INMUEBLES(

	NUMERO_CATASTRO				VARCHAR(100)				NOT NULL,
	SUPERFICIE					DOUBLE						NOT NULL,
	ANYO_CONSTRUCCION			DATE						NOT NULL,
	CONTINENTE					DOUBLE						NOT NULL,
	
	PRIMARY KEY(NUMERO_CATASTRO)
);


CREATE TABLE DIRECCIONES(

	NUMERO_CATASTRO				BIGINT						NOT NULL,
	POBLACION					VARCHAR(80)					NOT NULL,
	PROVENCIA					VARCHAR(80)					NOT NULL,
	TIPO_VIA					VARCHAR(80)					NOT NULL,
	NOMBRE						VARCHAR(100)				NOT NULL,
	NUMERO						BIT(1)						NOT NULL,
	ESCALERA					BIT(1)						NOT NULL,
	PISO						BIT(1)						NOT NULL,
	PUERTA						BIT(1)						NOT NULL,
	CODIGO_POSTAL				VARCHAR(80)					NOT NULL,
	
	PRIMARY KEY(NUMERO_CATASTRO),
	FOREIGN KEY (NUMERO_CATASTRO) REFERENCES CATASTROS(NUMERO_CATASTRO)
);



