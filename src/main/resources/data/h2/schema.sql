DROP TABLE VIVIENDA	IF EXISTS;

CREATE TABLE VIVIENDA(

	CODIGO						BIGINT						NOT NULL,
	RELACION					VARCHAR(80)					NOT NULL,
	USO							VARCHAR(80)					NOT NULL,
	TIPO						VARCHAR(80)					NOT NULL,
	TIPO_CONSTRUCCION			VARCHAR(80)					NOT NULL,
	SUPERFICIE					DOUBLE						NOT NULL,
	ANYO_CONSTRUCCION			DATE						NOT NULL,
	RESIDENTES					BIGINT						NOT NULL,
	UBICACION					VARCHAR(80)					NOT NULL,
	CODIGO_POSTAL				VARCHAR(80)					NOT NULL,
	PUERTA_BLINDAD				BIT(1)						NOT NULL,
	REJAS						BIT(1)						NOT NULL,
	ALARMA						BIT(1)						NOT NULL,
	CONTIENENTE					DOUBLE						NOT NULL,
	CONTENIDO					DOUBLE						NOT NULL,
	PRIMARY KEY(CODIGO)
);

